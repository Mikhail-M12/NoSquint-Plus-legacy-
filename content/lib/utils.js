function getWindowForTab(tab){
    //get window for tab
    var wm = Components.classes["@mozilla.org/appshell/window-mediator;1"]
        .getService(Components.interfaces.nsIWindowMediator);
    var browserEnumerator = wm.getEnumerator("navigator:browser");

    // Check each browser instance for our URL
    while (browserEnumerator.hasMoreElements()) {
        var browserWin = browserEnumerator.getNext();
        var tabbrowser = browserWin.gBrowser;

        // Check each tab of this browser instance
        var numTabs = tabbrowser.browsers.length;
        for (var index = 0; index < numTabs; index++) {
            var currentBrowser = tabbrowser.getBrowserAtIndex(index);
            if (currentBrowser === tab) {
                return browserWin;
                break;
            }
        }
    }
}

function isTabPrivate(tab_lb) {
    try {
        var tabWindow = getWindowForTab(tab_lb);
        return PrivateBrowsingUtils.isWindowPrivate(tabWindow);
    } catch (e) {
        // As per issue #73, for some unknown reason,
        // PBU.isWindowPrivate() can throw NS_NOINTERFACE.  I have never
        // been able to reproduce this on Linux, so perhaps this is
        // platform specific.  In any case, just blindly catch exceptions
        // thrown here and assume the window isn"t private if it throws.
        return false;
    }
}

function isPrivateTabOpen() {
    var wm = Components.classes["@mozilla.org/appshell/window-mediator;1"]
        .getService(Components.interfaces.nsIWindowMediator);
    var browserEnumerator = wm.getEnumerator("navigator:browser");

    while (browserEnumerator.hasMoreElements()) {
        var browserWin = browserEnumerator.getNext();
        if (PrivateBrowsingUtils.isWindowPrivate(browserWin)) {
            return true;
        }
    }

    return false;
}

//return browser instance
function getgBrowser() {
    var wm = Components.classes["@mozilla.org/appshell/window-mediator;1"]
        .getService(Components.interfaces.nsIWindowMediator);
    var mainWindow = wm.getMostRecentWindow("navigator:browser");
    return mainWindow ? mainWindow.gBrowser : null;
}

//call callback function for each tab (private also)
function iterTabs(callback) {
    var wm = Components.classes["@mozilla.org/appshell/window-mediator;1"]
        .getService(Components.interfaces.nsIWindowMediator);
    var browserEnumerator = wm.getEnumerator("navigator:browser");

    // Check each browser instance for our URL
    while (browserEnumerator.hasMoreElements()) {
        var browserWin = browserEnumerator.getNext();
        var tabbrowser = browserWin.gBrowser;

        // Check each tab of this browser instance
        var numTabs = tabbrowser.browsers.length;
        for (var index = 0; index < numTabs; index++) {
            var currentBrowser = tabbrowser.getBrowserAtIndex(index);
            callback(currentBrowser);
        }
    }
}

