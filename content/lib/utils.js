/* Given a FQDN, returns only the base domain, and honors two-level TLDs.
 * So for example, www.foo.bar.com returns bar.com, or www.foo.bar.co.uk
 * returns bar.co.uk.
 */
function getBaseDomainFromHost(host) {
    if (host.match(/^[\d.]+$/) != null)
    // IP address.
        return host;

    var parts = host.split(".");
    var level2 = parts.slice(-2).join(".");
    var level3 = parts.slice(-3).join(".");
    if (TLDs[level3])
        return parts.slice(-4).join(".");
    else if (TLDs[level2])
        return level3;
    return level2;
}

function* iter(...argums) {
    for (let i = 0; i < argums.length; i++) {
        var arg = argums[i];
        // duck typing
        if (arg.length !== undefined) {
            for (let idx = 0; idx < arg.length; idx++)
                yield arg[idx];
        } else {
            for (let key in arg)
                yield key
        }
    }
}

function* items(...argums) {
    for (let i = 0; i < argums.length; i++) {
        var arg = argums[i];
        for (var key in arg) {
			let value = arg[key];
            yield [key, value];
        }
    }
}